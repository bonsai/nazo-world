seen = set()
riddles = []

def add(q, a, cat, subcat=""):
    key = hashlib.md5((q+str(a)).encode()).hexdigest()
    if key not in seen and len(q) > 5:
        seen.add(key)
        riddles.append({"q": q.strip(), "a": a.strip(), "cat": cat, "subcat": subcat})

